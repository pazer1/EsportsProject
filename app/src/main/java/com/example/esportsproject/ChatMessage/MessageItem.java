package com.example.esportsproject.ChatMessage;

public class MessageItem {

    String name;
    String message;
    String time;
    String profileUrl;

    public MessageItem(String name, String message, String time, String profileUrl) {
        this.name = name;
        this.message = message;
        this.time = time;
        this.profileUrl = profileUrl;
    }
}
